# Pide la base y la altura de un rectángulo y muestra su área.
# Usa variables y operadores aritméticos.
AREA = 0.5
base = int(input("Introduce la base del rectángulo: "))
altura = int(input("Introduce la altura del rectángulo: "))
areaFinal = AREA * base * altura
print(f"El area final del rectángulo es: {areaFinal}")